package shop.product;

public class ProductDTO {
	private int productNo;
	private String productName;
	private String productDetail;
	private String productImage;
	private int productPrice;
	private String productType;
	private String productOpt1;
	private String productOpt2;
	
	public int getProductNo() {
		return productNo;
	}
	public void setProductNo(int productNo) {
		this.productNo = productNo;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getProductDetail() {
		return productDetail;
	}
	public void setProductDetail(String productDetail) {
		this.productDetail = productDetail;
	}
	public String getProductImage() {
		return productImage;
	}
	public void setProductImage(String productImage) {
		this.productImage = productImage;
	}
	public int getProductPrice() {
		return productPrice;
	}
	public void setProductPrice(int productPrice) {
		this.productPrice = productPrice;
	}
	public String getProductType() {
		return productType;
	}
	public void setProductType(String productType) {
		this.productType = productType;
	}
	public String getProductOpt1() {
		return productOpt1;
	}
	public void setProductOpt1(String productOpt1) {
		this.productOpt1 = productOpt1;
	}
	public String getProductOpt2() {
		return productOpt2;
	}
	public void setProductOpt2(String productOpt2) {
		this.productOpt2 = productOpt2;
	}
	
	
	
}
